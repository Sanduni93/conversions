class Conversions{
	
	public static void main(String args[]){
	
		double a = 3.12532;
		int b = (int) a;
		System.out.println("Example for Explicit conversion");
	    System.out.println("a:"+a);
		System.out.println("b:"+b);
		
		int c = 12;
		double d = c;
		System.out.println("Example for Implicit conversion");
		System.out.println("c:"+c);
		System.out.println("d:"+d);
		
		
		
		
	}
	
}